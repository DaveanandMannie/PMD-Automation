class Template:
	""" Printify API product values """
	def __init__(
			self,
			variants: list[int],
			price: int,
			name: str,
			blueprint: int,
			publish_data: dict[str: bool]
	):
		self.variants = variants
		self.price = price
		self.name = name
		self.blueprint = blueprint
		self.publish_data = publish_data

	def __str__(self):
		return f"{self.name} template"


